/// Scaffold a complete Aiken project directory at `output_dir`.
///
/// Creates:
/// - `aiken.toml` (project config)
/// - `validators/privacy_pool.ak` (pool + nullifier validators)
/// - `lib/pil.ak` (helper library)
///
/// Returns the paths of created files.
pub fn scaffold_aiken_project(
    output_dir: &std::path::Path,
) -> Result<Vec<std::path::PathBuf>, std::io::Error> {
    use std::fs;

    let validators_dir = output_dir.join("validators");
    let lib_dir = output_dir.join("lib");

    fs::create_dir_all(&validators_dir)?;
    fs::create_dir_all(&lib_dir)?;

    let toml_path = output_dir.join("aiken.toml");
    fs::write(&toml_path, generate_aiken_toml())?;

    let validator_path = validators_dir.join("privacy_pool.ak");
    fs::write(&validator_path, generate_pool_validator_aiken())?;

    let lib_path = lib_dir.join("pil.ak");
    fs::write(&lib_path, generate_aiken_lib())?;

    Ok(vec![toml_path, validator_path, lib_path])
}

/// Generate the Aiken helper library (lib/pil.ak).
fn generate_aiken_lib() -> String {
    r#"// PIL helper library for Aiken validators.
// Generated by pil-cardano — do not edit manually.

use aiken/crypto.{blake2b_256}
use aiken/collection/list

/// Check that a hash is exactly 32 bytes.
pub fn is_valid_hash(hash: ByteArray) -> Bool {
  builtin.length_of_bytearray(hash) == 32
}

/// Check that all elements in a list are unique (no duplicates).
pub fn all_unique(items: List<ByteArray>) -> Bool {
  list.length(items) == list.length(list.unique(items))
}

/// Verify a Merkle inclusion proof using Blake2b-256.
///
/// Note: Uses Blake2b because Poseidon is expensive in UPLC.
/// For production, consider off-chain Poseidon + on-chain attestation.
pub fn verify_merkle_inclusion_blake2b(
  leaf: ByteArray,
  path: List<(Int, ByteArray)>,
  root: ByteArray,
) -> Bool {
  let computed =
    list.foldl(
      path,
      leaf,
      fn(step, current) {
        let (direction, sibling) = step
        if direction == 0 {
          blake2b_256(builtin.append_bytearray(current, sibling))
        } else {
          blake2b_256(builtin.append_bytearray(sibling, current))
        }
      },
    )
  computed == root
}

/// Verify a threshold committee attestation.
///
/// Checks that at least `threshold` of the `required_signers` are present
/// in the transaction's `signatories` list.
pub fn verify_committee_attestation(
  proof_hash: ByteArray,
  signatories: List<ByteArray>,
  required_signers: List<ByteArray>,
  threshold: Int,
) -> Bool {
  let count =
    list.foldl(
      required_signers,
      0,
      fn(signer, acc) {
        if list.has(signatories, signer) {
          acc + 1
        } else {
          acc
        }
      },
    )
  count >= threshold && is_valid_hash(proof_hash)
}
"#
    .to_string()
}

/// Attempt to compile an Aiken project using the `aiken` CLI.
///
/// Requires `aiken` to be installed and available on PATH.
/// Returns the compiled UPLC output directory path on success.
pub fn compile_aiken_project(
    project_dir: &std::path::Path,
) -> Result<std::path::PathBuf, AikenBuildError> {
    let output = std::process::Command::new("aiken")
        .arg("build")
        .current_dir(project_dir)
        .output()
        .map_err(|e| AikenBuildError::NotInstalled(e.to_string()))?;

    if !output.status.success() {
        let stderr = String::from_utf8_lossy(&output.stderr).to_string();
        return Err(AikenBuildError::CompilationFailed(stderr));
    }

    let plutus_json = project_dir.join("plutus.json");
    if plutus_json.exists() {
        Ok(plutus_json)
    } else {
        // Older aiken versions output to artifacts/
        Ok(project_dir.join("artifacts"))
    }
}

/// Errors from Aiken project compilation.
#[derive(Debug, thiserror::Error)]
pub enum AikenBuildError {
    #[error("aiken CLI not found: {0}")]
    NotInstalled(String),
    #[error("aiken compilation failed: {0}")]
    CompilationFailed(String),
}

